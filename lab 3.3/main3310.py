        if "|" not in ввод:
            print("Ошибка: используйте формат 'Экзамен | Балл'. Попробуйте снова.")
            continue
            
        экзамен, балл = [x.strip() for x in ввод.split("|")]
    
        try:
            сданные_экзамены[экзамен] = int(балл)
        except ValueError:
            print(f"Ошибка: '{балл}' не является числом. Попробуйте снова.")
